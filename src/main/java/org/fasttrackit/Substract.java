package org.fasttrackit;

public class Substract {

    public int sub(int x, int y){

        if (x>y){
            return x-y;
        }
        else return y-x;
    }

    public double sub(double x, double y){

        if (x>y){
            return x-y;
        }
        else return y-x;

}
}



